public class VariableAssignmentDemo {

	public static void main(String[] args) {
		
		// dataType variableName = value;
		
		int age = 10;
		
		
		int num; // declaring a variable
		num = 8; // assigning the variable to a value (initializing)
		num = 9; // not initializing
		
		int a, b, c;
		a = 5;
		b = 6;
		c = 7;
		int x = 1, y = 2, z;
		
		System.out.println(age);
		
	}
}